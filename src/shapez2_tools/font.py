def silkscreen(
    text: str,
    origin_x: int,
    origin_y: int,
    *,
    layer: int = 0,
    scale: int = 2,
) -> list[Entity]:
    """Render text as trash-block pixel art for platform silk-screening.

    Each font pixel becomes ``scale`` tiles wide and 1 tile tall.  The origin
    is the top-left corner of the text in blueprint coordinates (Y increases
    upward, so rows extend downward from ``origin_y``).

    Characters not in the font are replaced with a filled rectangle.
    """
    entities: list[Entity] = []
    fallback = tuple([((1 << CELL_WIDTH) - 1)] * CELL_HEIGHT)

    for ci, ch in enumerate(text):
        glyph = GLYPHS.get(ch, fallback)
        char_x = origin_x + ci * ADVANCE * scale
        for row_idx, row_bits in enumerate(glyph):
            if not row_bits:
                continue
            tile_y = origin_y - row_idx
            for px in range(CELL_WIDTH):
                if row_bits & (1 << px):
                    for sx in range(scale):
                        entities.append(Entity(
                            x=char_x + px * scale + sx,
                            y=tile_y,
                            type=TRASH_TYPE,
                            layer=layer,
                        ))
    return entities
